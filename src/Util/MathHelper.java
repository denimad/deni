public class MathHelper 
{
	/**
	 * a -> c
	 * b -> ?
	 * @param a
	 * @param b
	 * @param c
	 * @return 
	 */
	public static float directRuleOfThree(float a, float b, float c)
	{
		return (b * c)/a;
	}
}
